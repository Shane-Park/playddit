package feedtest.vo;

public class FeedTestVO {
		
	private int feed_no;
	private String user_id;
	private String feed_cont;
	private String feed_date;
	private String feed_pic;
	
	public FeedTestVO() {
		
	}

	public int getFeed_no() {
		return feed_no;
	}

	public void setFeed_no(int feed_no) {
		this.feed_no = feed_no;
	}

	public String getUser_id() {
		return user_id;
	}

	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}

	public String getFeed_cont() {
		return feed_cont;
	}

	public void setFeed_cont(String feed_cont) {
		this.feed_cont = feed_cont;
	}

	public String getFeed_date() {
		return feed_date;
	}

	public void setFeed_date(String feed_date) {
		this.feed_date = feed_date;
	}

	public String getFeed_pic() {
		return feed_pic;
	}

	public void setFeed_pic(String feed_pic) {
		this.feed_pic = feed_pic;
	}
	
	
	
	
}
