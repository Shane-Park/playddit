package join.vo;

public class TermsVO {
	private int terms_use;
	private int terms_seq;
	private int terms_essen;
	private String terms_cont;
	private String terms_title;
	private int terms_no;
	
	public TermsVO() {

	}
	
	public int getTerms_use() {
		return terms_use;
	}
	public void setTerms_use(int terms_use) {
		this.terms_use = terms_use;
	}
	public int getTerms_seq() {
		return terms_seq;
	}
	public void setTerms_seq(int terms_seq) {
		this.terms_seq = terms_seq;
	}
	public int getTerms_essen() {
		return terms_essen;
	}
	public void setTerms_essen(int terms_essen) {
		this.terms_essen = terms_essen;
	}
	public String getTerms_cont() {
		return terms_cont;
	}
	public void setTerms_cont(String terms_cont) {
		this.terms_cont = terms_cont;
	}
	public String getTerms_title() {
		return terms_title;
	}
	public void setTerms_title(String terms_title) {
		this.terms_title = terms_title;
	}
	public int getTerms_no() {
		return terms_no;
	}
	public void setTerms_no(int terms_no) {
		this.terms_no = terms_no;
	}
	
}
