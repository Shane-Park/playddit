package news.vo;

public class NewsVO {
	private String news_pic;
	private String news_date;
	private String news_cont;
	private String news_title;
	private int news_no;
	
	
	public String getNews_pic() {
		return news_pic;
	}
	public void setNews_pic(String news_pic) {
		this.news_pic = news_pic;
	}
	public String getNews_date() {
		return news_date;
	}
	public void setNews_date(String news_date) {
		this.news_date = news_date;
	}
	public String getNews_cont() {
		return news_cont;
	}
	public void setNews_cont(String news_cont) {
		this.news_cont = news_cont;
	}
	public String getNews_title() {
		return news_title;
	}
	public void setNews_title(String news_title) {
		this.news_title = news_title;
	}
	public int getNews_no() {
		return news_no;
	}
	public void setNews_no(int news_no) {
		this.news_no = news_no;
	}
	
	
	
}
